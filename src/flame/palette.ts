export function generateFireDragon(): Palette {
  // Each set of 8 bytes represents a color in the palette
  // 00RRGGBB
  const palette: Palette = [];
  for (let i = 0; i < FIRE_DRAGON_PALETTE_STR.length; i += 8) {
    const color = FIRE_DRAGON_PALETTE_STR.slice(i, i + 8);
    const r = parseInt(color.slice(2, 4), 16) / 255;
    const g = parseInt(color.slice(4, 6), 16) / 255;
    const b = parseInt(color.slice(6, 8), 16) / 255;
    palette.push([r, g, b]);
  }

  return palette;
}
